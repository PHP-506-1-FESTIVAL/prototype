제1조 (목적)
이 약관은 전기통신사업법령 및 정보통신망 이용촉진 및 정보보호 등에 관한 법령에 의하여 ㈜마실가실(이하"회사"라 합니다)이 제공하는 서비스를 이용함에 있어 이용조건 및 절차에 관한 기본적인 사항을 규정하는 것을 목적으로 합니다.

제2조 (약관의 효력 및 변경)
이 약관은 그 내용을 회사가 서비스 화면을 통해 게시하고 이용자가 이에 동의를 함으로써 효력을 발생합니다.
회사는 약관의 규제 등에 관한 법률, 전기통신사업법령, 정보통신망이용촉진 및 정보보호 등에 관한 법률 등 관련법을 위배하지 않는 범위에서 이 약관을 정할 수 있으며 필요 시 약관을 개정할 수 있습니다. 그리고, 변경된 약관에 이의가 있는 회원은 회사가 정한 양식에 따라 언제든지 탈퇴할 수 있습니다.
회사가 약관을 개정할 경우에는 적용일자 및 개정 사유를 명시하여 현행 약관과 함께 서비스 초기화면에 그 적용일자 7일(단, 이용자에게 불리한 변경 또는 중대한 사항의 변경은 최소 30일) 이전부터 적용일자 전일까지 공지하며, 제1항과 같은 방법으로 효력이 발생합니다.
약관을 개정할 경우에는 그 개정 약관은 그 적용일자 이후에 체결되는 계약에만 적용되고 그 이전에 이미 체결된 계약에 대해서는 개정 전의 약관 조항이 그대로 적용됨을 원칙으로 합니다. 다만, 개정 약관의 적용을 원하는 회원의 동의 절차의 편의를 위하여 이미 계약을 체결한 이용자가 동조 제 3항의 공지에도 불구하고 개정 약관 조항의 적용을 받지 않기를 원한다는 이의를 표시하지 않은 경우에는 동조 1항에 동의한 것으로 간주되며 개정 약관이 적용됩니다
제3조 (약관외 준칙)
