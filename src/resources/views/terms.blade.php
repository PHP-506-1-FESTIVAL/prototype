회사는 다음 각 호에 해당하는 경우 서비스 제공을 중지할 수 있습니다.
가) 서비스용 설비의 보수 등 공사로 인한 부득이한 경우
나) 전기통신사업법에 규정된 기간통신사업자가 전기통신 서비스를 중지했을 경우
다) 설비 장애 또는 이용 폭주 등으로 서비스 이용에 지장이 있는 경우
라) 기타 불가항력적 사유가 있는 경우
회사는 이용자에게 아래와 같은 서비스를 제공합니다.
가) 공공 API 기반 축제 정보 제공 서비스
나) 게시판형 또는 댓글형 서비스
다) 기타 회사가 자체 개발하거나 다른 회사와의 제휴계약을 통해 이용자에게 제공하는 일체의 서비스
회사는 일부 서비스 이용 시 이용자에게 안정적인 서비스를 제공하기 위해 이용자의 PC 및 네트워크 자원을 활용하여 다른 이용자에게 데이터를 전송하는 분산 솔루션 기술을 사용할 수 있습니다.
서비스의 내용, 이용방법, 이용시간에 대하여 변경이 있는 경우에는 변경될 서비스의 내용 및 제공일자 등은 그 변경 전 7일 이상 해당 서비스 초기화면에 게시하여야 합니다.
제9조 (이용자에 대한 통지 및 고지)
회사는 회원이 서비스 이용 중 필요하다고 인정되는 다양한 정보에 대해서 전자메일이나 기타 방법 등으로 회원에게 정보를 제공할 수 있습니다.
회사는 불특정다수 회원에 대한 통지 및 고지의 경우 1주일 이상 게시판에 게시함으로써 개별 통지 및 고지에 갈음할 수 있습니다.
제10조 (이용자의 개인정보, ID 및 비밀번호에 대한 의무)
회원은 서비스 이용을 위해 등록할 경우 현재의 사실과 일치하는 완전한 정보(이하 "등록정보"라 한다)를 제공하여야 합니다. 또한 등록정보에 변경이 발생할 경우 즉시 갱신하여야 합니다. 회원이 제공하는 정보의 내용이 허위인 것으로 판명되거나, 그러하다고 의심할만한 합리적인 사유가 발생할 경우 회사는 회원의 본 서비스 사용을 일부 또는 전부 중지할 수 있으며, 이로 인해 발생하는 불이익에 대해 책임을 지지 않습니다.
회원이 서비스 사용을 위한 등록절차를 완료하면 아이디, 사용자 계정과 비밀 번호를 받게 됩니다. (단, SNS 계정 연동 로그인 시 별도로 아이디, 비밀 번호를 부여하지 않습니다). 회원은 회원의 아이디, 비밀번호 관리를 위해 회원의 승인 없이 비밀번호, 아이디가 사용되는 문제가 발생하면 즉시 회사에 신고하여야 하고, 매 접속 종료 시 확실히 로그아웃을 하여야 합니다.
회원이 불법으로 타인의 개인정보를 게시, 전송, 입수하였거나 전자메일 기타 다른 수단에 의하여 게시, 전송 또는 입수한 모든 형태의 정보에 대하여는 회원이 책임을 부담하며 회사는 책임을 부담하지 아니합니다.
회사가 관계법령 및 "개인정보취급방침"에 의해서 그 책임을 지는 경우를 제외하고, 자신의 아이디와 비밀번호에 관한 관리책임은 각 회원에게 있습니다.
회원은 자신의 아이디 및 비밀번호를 제3자에게 이용하게 해서는 안됩니다.
회원은 자신의 아이디 및 비밀번호를 도난 당하거나 제3자가 사용하고 있음을 인지한 경우에는 바로 회사에 통보하고, 회사의 안내가 있는 경우에는 그에 따라야 합니다.
제11조 (광고 게재)
회사는 서비스의 운용과 관련하여 서비스 화면, 홈페이지, 이메일 등에 광고 등을 게재할 수 있습니다.
회사는 서비스상에 게재되어 있는 광고주의 판촉활동에 회원이 참여하거나 거래의 결과로서 발생하는 손실 또는 손해에 대해 책임을 지지 않습니다.
제12조 (회원 게시물)
게시물에 대한 저작권을 포함한 모든 권리 및 책임은 이를 게시한 회원에게 있으며 회사는 회사의 서비스 내의 게재권을 갖습니다. 단, 회사가 제공한 동영상, 이미지, 오디오 등의 멀티미디어 콘텐츠를 이용하여 제작된 게시물의 경우, 원본 콘텐츠의 사용 부분에 대해서는 본 항목에서 규정한 회원의 ‘권리 및 책임’의 적용은 제외되며, 회원이 추가한 부분의 콘텐츠 및 게시물에 대하여 본 조 본 항 및 2, 3 항이 적용됩니다.
회사는 이용자가 서비스에 게재한 정보의 정확성 및 신뢰도 등 내용에 대해서는 책임을 지지 않습니다.
회사는 회원이 서비스에 게시, 게재하거나 서비스를 통해 전송, 수신한 내용물 및 그 결과에 대하여 책임지지 않으며, 아래 사항에 해당하는 경우 게시물을 임의로 삭제할 수 있습니다.
가) 타인을 비방하거나, 프라이버시를 침해하거나, 명예를 손상시키는 경우
나) 공공질서 및 미풍양속에 위반되는 경우
다) 서비스의 안정적인 운영에 지장을 주거나 줄 우려가 있는 경우
라) 범죄행위와 관련된다고 판단되는 경우
마) 회사의 지적재산권, 타인의 지적재산권 등 기타 권리를 침해하는 경우
바) 특정 제품의 선전 등 기타 상용 목적으로 게시되는 내용인 경우
사) 입력된 정보를 무단 개조하는 경우
아) 타 회원의 회원 아이디, 비밀번호를 도용하는 경우
자) 게시판의 목적에 맞지 않는 사이버 시위 및 도배 목적의 내용인 경우
차) 기타 관계 법령에 위반된다고 판단되는 경우
카) 당사에서 규정한 게시 기간을 초과한 경우
제13조 (회사가 제공하는 콘텐츠를 이용한 회원 저작물의 게시)
회사가 제공하는 콘텐츠라 함은 회사가 소유하거나, 회사가 제3자와 계약을 통하여 회원에게 제공하는 콘텐츠로써 동영상, 이미지, 오디오 등의 멀티미디어 콘텐츠를 의미합니다. 이때 회사에 의하여 제공되지 않는 콘텐츠는 본 약관의 제12조에 규정된 회원 게시물을 의미합니다.
회사가 제공하는 콘텐츠를 이용한 회원의 저작물이라 함은 회사가 제공하는 멀티미디어 편집기를 이용하여 동영상, 이미지, 오디오 파일의 편집, 자막 삽입 등 회원 저작 활동의 결과물을 말합니다. 전체 또는 일부가 회사가 제공한 콘텐츠 및 저작도구를 이용하여 만들어지는 모든 회원 저작물이 이에 해당합니다.
회원이 회사가 제공한 콘텐츠와 다른 콘텐츠를 편집하여 새로운 저작물을 만들었을 경우 회원은 해당 콘텐츠에 대한 생성, 게재 및 삭제 권한을 가지나 그 이외의 모든 권한은 부여되지 않으며, 회사에 귀속됩니다. 회원이 생성, 게재 및 삭제 이외의 권한을 갖지 못하는 것은 회사가 제공하는 콘텐츠에 대한 저작권을 포함한 제반 권리를 회원에게 양도하지 않았기 때문입니다.
회원들은 회사가 제공한 동영상 등을 편집함에 있어 동영상의 인물 또는 내용을 변경하여 원저작권자들의 저작권 및 초상권자들의 초상권을 침해하거나 타인의 명예를 훼손하는 등의 불법적인 행위를 하여서는 안됩니다.
회사가 제공한 콘텐츠에 회원이 추가 또는 삭제하여 저작한 저작물 전체 또는 일부가 본 조 4항 및 제12조 3항의 각 항목 중 어느 일부에 해당되는 경우, 회사는 동 조항에 의해 해당 게시물을 임의로 삭제할 수 있습니다.
회사가 제공한 콘텐츠를 이용한 회원의 저작물에 대하여 회사는 회사가 운영하는 사이트, 회사가 정한 타 사이트 및 다른 형태(매체)를 통하여 서비스할 수 있는 권리가 있으며, 회원은 저작, 편집 등의 활동에 참여함과 함께 이에 동의하는 것으로 합니다. 만일 회원이 이를 원하지 않을 경우 본 조 3항에 의해 주어진 회원의 삭제 권리를 이용하여 이를 방지할 수 있습니다. 이때, 회원이 해당 콘텐츠를 삭제하기 전에 회사가 이를 다른 사이트 및 다른 형태(매체)로 전환하여 서비스하고, 회원의 삭제에 대한 반영을 실시간으로 처리할 수 없을 경우 이를 차후에 반영하여 서비스 하는 것으로 회사의 의무를 다하는 것으로 합니다.
회원이 회사가 제공한 콘텐츠를 이용하여 새로운 콘텐츠를 제작한 후, 회사가 제공한 콘텐츠에 대한 권리 관계에 변화가 발생하여 이를 더 이상 서비스 할 수 없을 경우 이를 이용한 회원에게 개별적으로 통보하지 않으며, 회사가 운영하는 사이트를 통하여 공지한 후 해당 콘텐츠를 삭제 처리할 수 있습니다.
회사의 정책 변경에 의하여 해당 서비스가 중단(또는 종료)될 경우 회사는 이를 회사가 운영하는 사이트를 통하여 7일 이상 공지한 후 해당 서비스를 중지하고, 관련 콘텐츠를 삭제할 수 있습니다.
회사의 콘텐츠를 이용하여 회원의 저작 활동으로 만들어진 콘텐츠에 대하여, 회사는 회원의 편의를 위하여 링크 등 회사가 정한 방법 및 절차를 통하여 다른 사이트(블로그, 개인홈페이지 등)에서 이용할 수 있도록 할 수 있습니다. 이때, 회사가 정한 방법 및 절차를 악용하여 회사의 사전 서면 승인 없이 관련 데이터를 조직적, 기계적으로 수집하여 데이터베이스의 형태로 보관하거나 이를 일체의 상업적 또는 그에 준하는 목적으로 사용하는 것(포털 또는 검색 사이트 등에서 본 서비스와 관련된 회사의 메타데이터를 수집, 가공하여 서비스하는 것, 또는 회사가 제공하는 이미지, 제목, 내용, 링크의 형태로 검색 결과 페이지에서 서비스 되는 것)을 금지합니다. 이를 어길 경우 회사는 제15조 1, 2항에 의거 관련된 회원, 또는 사업자에 대하여 의법 처리할 수 있습니다.
제14조 (포인트)
회사는 서비스 이용실적 등에 따라 회원에게 포인트를 지급할 수 있으며, 회사가 정한 기간에 따라 주기적으로 소멸할 수 있습니다.
포인트의 적립, 사용, 소멸시효기간 등 상세 규정은 “유료/포인트 회원약관”에 따르며, “유료/포인트 회원 약관”에 명시하지 않은 사항은 서비스 안내 페이지에 게재된 내용에 따릅니다.
제15조 (지적소유권의 보호)
회사가 작성한 저작물에 대한 저작권 및 기타 지적재산권은 모두 회사에 귀속하며 서비스 외에서의 복제, 전송, 배포 등의 일체의 이용은 금지됩니다.
회원은 서비스를 이용하여 얻은 정보를 가공, 판매하는 행위 등 서비스에 게재된 자료를 상업적으로 이용할 수 없습니다.
회사가 제공하는 모든 콘텐츠 및 서비스에 대해서는 회원이 단순 링크의 방법이나 기타 전자적인 방법을 통하여 본 사이트 외에서 이용하는 것은 상업적 목적 여부를 불문하고 금지됩니다. 단, 회사가 정한 특정한 서비스의 경우 제외되며, 이때도 회사가 정한 방법 및 절차를 통하여 이용하여야 합니다.
제4장 개인정보보호
제16조 (개인정보보호정책)
회사의 개인정보보호정책은 정보통신망 이용촉진 및 정보보호 등에 관한 법률의 제규정을 준수합니다. 관련 내용은 2012년11월2일부터 시행되는 '개인정보취급방침'에 따라 작성되어, 사이트에 고지하고 있습니다.

제5장 각 당사자의 의무
제17조 (회사의 의무)
회사는 법령과 이 약관이 금지하는 행위를 하지 않으며 이 약관이 정하는 바에 따라 지속적이고, 안정적으로 서비스를 제공하는 데 최선을 다하여야 합니다.
회사는 이용자가 안전하게 서비스를 이용할 수 있도록 이용자의 개인정보(신용정보 포함)의 보호, 관리를 위해 필요한 조치를 취합니다.
회사는 이용자가 원하지 않는 영리 목적의 광고성 전자우편을 발송하지 않습니다.
회사는 회원 개인 정보를 본인의 승낙 없이 제3자에게 누설, 배포하지 않는 것을 원칙으로 합니다. 단, 아래 사항에 해당하는 경우는 예외입니다.
가) 전기통신기본법 등 법률의 규정에 의해 국가기관의 요구가 있는 경우
나) 범죄에 대한 수사상의 목적이 있거나 방송통신심의위원회의 요청이 있는 경우
다) 기타 관계법령에서 정한 절차에 따른 요청이 있는 경우
회사는 업무와 관련하여 회원 전체 또는 일부의 개인 정보에 관한 자료를 작성하여 이를 사용할 수 있고, 서비스를 통하여 회원의 컴퓨터에 쿠키를 전송할 수 있습니다.
제18조 (회원의 의무)
회원은 이 약관에서 규정하는 모든 사항과 서비스 이용안내 및 주의사항 등을 준수하여야 합니다.
회원은 등록 양식에서 요구되는 내용들에 대해 진실되고 정확하며 현재의 사실과 일치되는 완전한 정보를 제공하고 유지합니다.
회원은 자신의 아이디와 비밀번호를 관리할 의무를 가지고 있으며, 그에 대한 회원의 관리 소홀로 인하여 발생하는 모든 결과에 대하여는 회원에게 책임이 있습니다.
회원은 자신의 아이디 및 비밀번호를 제3자에게 이용하게 해서는 안되며, 아이디 및 비밀번호를 도난 당하거나 제3자가 사용하고 있음을 인지한 경우에는 바로 회사에 통보하고 회사의 안내가 있는 경우에는 그에 따라야 합니다.
회원은 회사가 제공하는 모든 서비스 및 콘텐츠를 회사의 사전 승낙 없이 상업적인 목적을 위하여 서비스를 이용할 수 없습니다.
