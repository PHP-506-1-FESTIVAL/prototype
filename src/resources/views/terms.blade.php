바) 제17조 (회원의 의무) 제 4항 내지 9항의 의무를 해태한 경우
회사가 회원자격 및 서비스 이용을 제한 정지시킨 후 그 사유가 시정되지 아니하는 경우 회사는 회원자격을 상실시킬 수 있습니다.
회사가 회원자격을 상실시키는 경우에는 회원등록을 말소합니다. 이 경우 회원에게 이를 통지하고, 회원등록 말소 전에 소명할 기회를 부여합니다.
회원이 서비스를 이용하는 도중, 연속하여 1년 동안 서비스를 이용하기 위한 로그인한 기록이 없는 경우, 회사는 관련법에 의거하여 이용자의 회원자격을 상실시킬 수 있으며, 이 경우 회사는 해당 회원의 개인 정보를 즉시 삭제합니다.
