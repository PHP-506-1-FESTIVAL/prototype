회원은 회원가입 신청 시 기재한 사항이 변경되었을 경우 1항의 방법으로 "회사"에 변경사항을 알려야 하며, 이때 변경하지 않은 정보로 인해 발생되는 불이익에 대하여 회사는 책임지지 않습니다.
제7조 (회원의 이용계약 해지 탈퇴 및 자격 상실, 서비스 이용제한 등)
회원은 탈퇴를 희망하는 경우 탈퇴를 요청할 수 있으며 회사는 정한 양식에 의하여 회원 탈퇴를 즉시 처리합니다.
회사는 회원이 자발적으로 계약해지를 했을 경우 관련법에 의거 계약 해지일로부터 1개월 이내에 모든 고객정보를 삭제합니다.
계약해지 후 재가입은 30일 이내에는 불가하며 동일 아이디로의 재가입 또한 불가능합니다.
회원이 다음 각호의 사유에 해당하는 경우, 회사는 회원자격을 제한 및 정지시킬 수 있습니다.
가) 가입 신청 시 허위 내용을 등록한 경우
나) 다른 사람의 서비스 이용을 방해하거나, 타인의 명예를 훼손하거나 그 정보를 도용하는 등 전자거래 및 정보통신서비스 이용 질서를 위협하는 경우
다) 제공되는 정보를 변경하는 등 회사의 운영을 방해한 경우
라) 법령과 본 약관이 금지하거나 미풍양속에 반하는 행위를 하는 경우 및 회사가 정한 이용 조건에 위배되는 경우
